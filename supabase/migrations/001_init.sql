CREATE OR REPLACE VIEW student_attendance_summary AS
SELECT
  u.id AS student_id,
  u.full_name AS student_name,
  u.school_id,
  COUNT(a.id) AS total_sessions,
  COUNT(*) FILTER (WHERE a.status = 'PRESENT') AS present_count,
  COUNT(*) FILTER (WHERE a.status = 'LATE') AS late_count,
  COUNT(*) FILTER (WHERE a.status = 'ABSENT') AS absent_count,
  ROUND(
    (
      (COUNT(*) FILTER (WHERE a.status = 'PRESENT')
      + COUNT(*) FILTER (WHERE a.status = 'LATE') * 0.5)::NUMERIC
      /
      NULLIF(COUNT(a.id), 0)
    ) * 100.0,
    2
  ) AS attendance_percentage
FROM "user" u
LEFT JOIN attendance a ON a.student_id = u.id
WHERE u.role = 'student'
GROUP BY u.id, u.full_name, u.school_id;

CREATE OR REPLACE VIEW student_grade_summary AS
SELECT
  u.id AS student_id,
  u.full_name AS student_name,
  u.school_id,
  COUNT(s.id) AS submissions_count,
  ROUND(AVG(s.percentage), 2) AS average_percentage
FROM "user" u
LEFT JOIN assignment_submission s ON s.user_id = u.id
WHERE u.role = 'student'
GROUP BY u.id, u.full_name, u.school_id;

CREATE OR REPLACE VIEW parent_child_overview AS
SELECT
  p.id AS parent_id,
  p.full_name AS parent_name,
  s.id AS student_id,
  s.full_name AS student_name,
  s.school_id,
  att.attendance_percentage,
  grd.average_percentage,
  COALESCE(f.pending_fees_count, 0) AS pending_fees_count,
  COALESCE(f.pending_fees_amount, 0) AS pending_fees_amount
FROM parent_student_link l
JOIN "user" p ON p.id = l.parent_id
JOIN "user" s ON s.id = l.student_id
LEFT JOIN student_attendance_summary att ON att.student_id = s.id
LEFT JOIN student_grade_summary grd ON grd.student_id = s.id
LEFT JOIN (
  SELECT
    student_id,
    COUNT(*) FILTER (WHERE status IN ('PENDING', 'OVERDUE')) AS pending_fees_count,
    COALESCE(SUM(amount) FILTER (WHERE status IN ('PENDING', 'OVERDUE')), 0) AS pending_fees_amount
  FROM fees
  GROUP BY student_id
) f ON f.student_id = s.id;

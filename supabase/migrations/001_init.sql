CREATE OR REPLACE FUNCTION fn_validate_parent_student_link()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
  parent_role user_role;
  student_role user_role;
  parent_school INTEGER;
  student_school INTEGER;
BEGIN
  SELECT role, school_id INTO parent_role, parent_school FROM "user" WHERE id = NEW.parent_id;
  SELECT role, school_id INTO student_role, student_school FROM "user" WHERE id = NEW.student_id;

  IF parent_role IS NULL OR student_role IS NULL THEN
    RAISE EXCEPTION 'Parent or student user does not exist';
  END IF;

  IF parent_role <> 'parent' THEN
    RAISE EXCEPTION 'parent_id % is not a parent user', NEW.parent_id;
  END IF;

  IF student_role <> 'student' THEN
    RAISE EXCEPTION 'student_id % is not a student user', NEW.student_id;
  END IF;

  IF parent_school <> student_school THEN
    RAISE EXCEPTION 'Parent and student must belong to the same school';
  END IF;

  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_validate_classroom_membership()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
  user_role_val user_role;
  user_school INTEGER;
  classroom_school INTEGER;
BEGIN
  SELECT role, school_id INTO user_role_val, user_school FROM "user" WHERE id = NEW.user_id;
  SELECT school_id INTO classroom_school FROM classroom WHERE id = NEW.classroom_id;

  IF user_role_val IS NULL OR classroom_school IS NULL THEN
    RAISE EXCEPTION 'User or classroom does not exist';
  END IF;

  IF user_role_val <> 'student' THEN
    RAISE EXCEPTION 'Only student users can join classroom memberships';
  END IF;

  IF NEW.role <> 'student' THEN
    RAISE EXCEPTION 'classroom_membership.role must be student';
  END IF;

  IF user_school <> classroom_school THEN
    RAISE EXCEPTION 'Student and classroom must be from the same school';
  END IF;

  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_validate_attendance_student_session()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
  student_role_val user_role;
  membership_exists BOOLEAN;
BEGIN
  SELECT role INTO student_role_val FROM "user" WHERE id = NEW.student_id;

  IF student_role_val <> 'student' THEN
    RAISE EXCEPTION 'Attendance can only be recorded for student users';
  END IF;

  SELECT EXISTS (
    SELECT 1
    FROM class_session cs
    JOIN classroom_membership cm
      ON cm.classroom_id = cs.classroom_id
     AND cm.user_id = NEW.student_id
     AND cm.role = 'student'
    WHERE cs.id = NEW.session_id
  ) INTO membership_exists;

  IF NOT membership_exists THEN
    RAISE EXCEPTION 'Student % is not a member of the classroom for session %', NEW.student_id, NEW.session_id;
  END IF;

  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_validate_assignment_submission()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
  student_role_val user_role;
  assignment_total INTEGER;
  assignment_classroom BIGINT;
  membership_exists BOOLEAN;
BEGIN
  SELECT role INTO student_role_val FROM "user" WHERE id = NEW.user_id;

  IF student_role_val <> 'student' THEN
    RAISE EXCEPTION 'Only student users can submit assignments';
  END IF;

  SELECT total_marks, classroom_id
    INTO assignment_total, assignment_classroom
    FROM assignment
   WHERE id = NEW.assignment_id;

  IF assignment_total IS NULL THEN
    RAISE EXCEPTION 'Assignment % does not exist', NEW.assignment_id;
  END IF;

  IF NEW.total <> assignment_total THEN
    RAISE EXCEPTION 'Submission total (%) must match assignment total_marks (%)', NEW.total, assignment_total;
  END IF;

  SELECT EXISTS (
    SELECT 1
    FROM classroom_membership cm
    WHERE cm.classroom_id = assignment_classroom
      AND cm.user_id = NEW.user_id
      AND cm.role = 'student'
  ) INTO membership_exists;

  IF NOT membership_exists THEN
    RAISE EXCEPTION 'Student % is not a member of assignment classroom %', NEW.user_id, assignment_classroom;
  END IF;

  RETURN NEW;
END;
$$;

DO $$

-- Idempotent migration for Supabase Postgres

CREATE EXTENSION IF NOT EXISTS pgcrypto;

DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'user_role') THEN
    CREATE TYPE user_role AS ENUM ('parent', 'student');
  END IF;

  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'attendance_status') THEN
    CREATE TYPE attendance_status AS ENUM ('PRESENT', 'ABSENT', 'LATE');
  END IF;

  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'fee_status') THEN
    CREATE TYPE fee_status AS ENUM ('PAID', 'PENDING', 'OVERDUE');
  END IF;
END $$;

CREATE TABLE IF NOT EXISTS "user" (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username TEXT NOT NULL UNIQUE,
  password_hash TEXT NOT NULL,
  role user_role NOT NULL,
  full_name TEXT NOT NULL,
  school_id INTEGER NOT NULL CHECK (school_id > 0),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS parent_student_link (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  parent_id BIGINT NOT NULL REFERENCES "user" (id) ON DELETE CASCADE,
  student_id BIGINT NOT NULL REFERENCES "user" (id) ON DELETE CASCADE,
  UNIQUE (parent_id, student_id)
);

CREATE TABLE IF NOT EXISTS classroom (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  school_id INTEGER NOT NULL CHECK (school_id > 0),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS classroom_membership (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  classroom_id BIGINT NOT NULL REFERENCES classroom (id) ON DELETE CASCADE,
  user_id BIGINT NOT NULL REFERENCES "user" (id) ON DELETE CASCADE,
  role user_role NOT NULL DEFAULT 'student',
  CHECK (role = 'student'),
  UNIQUE (classroom_id, user_id)
);

CREATE TABLE IF NOT EXISTS class_session (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  classroom_id BIGINT NOT NULL REFERENCES classroom (id) ON DELETE CASCADE,
  session_date DATE NOT NULL,
  topic TEXT NOT NULL,
  UNIQUE (classroom_id, session_date)
);

CREATE TABLE IF NOT EXISTS attendance (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  student_id BIGINT NOT NULL REFERENCES "user" (id) ON DELETE CASCADE,
  session_id BIGINT NOT NULL REFERENCES class_session (id) ON DELETE CASCADE,
  status attendance_status NOT NULL,
  UNIQUE (student_id, session_id)
);

CREATE TABLE IF NOT EXISTS assignment (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  classroom_id BIGINT NOT NULL REFERENCES classroom (id) ON DELETE CASCADE,
  title TEXT NOT NULL,
  total_marks INTEGER NOT NULL CHECK (total_marks > 0),
  due_date DATE NOT NULL
);

CREATE TABLE IF NOT EXISTS assignment_submission (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  assignment_id BIGINT NOT NULL REFERENCES assignment (id) ON DELETE CASCADE,
  user_id BIGINT NOT NULL REFERENCES "user" (id) ON DELETE CASCADE,
  score NUMERIC(6,2) NOT NULL CHECK (score >= 0),
  total INTEGER NOT NULL CHECK (total > 0),
  percentage NUMERIC(5,2)
    GENERATED ALWAYS AS (
      CASE
        WHEN total > 0 THEN ROUND((score / total::NUMERIC) * 100.0, 2)
        ELSE NULL
      END
    ) STORED,
  submitted_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CHECK (score <= total),
  UNIQUE (assignment_id, user_id)
);

CREATE TABLE IF NOT EXISTS fees (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  student_id BIGINT NOT NULL REFERENCES "user" (id) ON DELETE CASCADE,
  amount NUMERIC(10,2) NOT NULL CHECK (amount > 0),
  due_date DATE NOT NULL,
  status fee_status NOT NULL,
  paid_on DATE NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CHECK (
    (status = 'PAID' AND paid_on IS NOT NULL)
    OR
    (status <> 'PAID')
  )
);

CREATE INDEX IF NOT EXISTS idx_parent_student_link_parent_id ON parent_student_link (parent_id);
